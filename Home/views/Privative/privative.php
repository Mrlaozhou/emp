<script>
	$(function(){

		// 存储当前幻灯片索引
		var i=0;

		// 自动切换函数
		function autoChange(){
			if(i<2)
			{
				i++;
			}
			else
			{
				i=0;
			}
			cut(i);
		}

		// 定义计时器
		var s=setInterval(function(){autoChange()},8000);

		function cut(i){
			var w=$('.carousel img').width();
			$('.carousel-inner').stop().animate({'margin-left':-i*w+'px'},800);
			$('.carousel-index span').eq(i).addClass('active').siblings().removeClass('active');
			clearInterval(s);
			s=setInterval(function(){autoChange()},8000);
		}

		// 轮播图dot click事件
		$('.carousel-index span').click(function(){
			i=$('.carousel-index span').index($(this));
			cut(i);
		})


		// section 分类标签切换
		$('.classify b').click(function(){
			$(this).addClass('active').siblings().removeClass('active');
		})
	})
</script>